int compare_U32(U32 a, U32 b, int bits)
{
	a = a >> bits;
	b = b >> bits;
	return (a > b) - (a < b);
}



U32 phb_sub_getPinyinValue(U8 *str)
{
	register U32 h;
	register U8 *p;
	int i = 0;

	p = (U8 *)str ;

	for ( h = 0; *p && i < 6; p++ )
	{
		h = h << 5 | ((*p - 'a' + 1) & 0x1F); 
		i++;
	}

	for ( ; i < 6; i++ )
		h = h << 5;

	return h;
}

// ÊäÈëÆ´Òô, ·µ»ØÆ´Òô¶ÔÓ¦µÄºº×Ö 
int phb_idxSearch_getChLtr(U8 *pinyin, const char** ChLtr)
{
	U32 value = phb_sub_getPinyinValue(pinyin);
	U32 start = 0, end = TOTAL_PHONETIC - 1, middle = (start + end) / 2;
	int bits = 30 - 5 * strlen(pinyin);

	*ChLtr = NULL;

	if (strlen(pinyin) > 6)
		return -1;

	if (compare_U32(phonetic_hash[start] , value, bits) > 0)
		return PHB_FAIL;

	if (compare_U32(value, phonetic_hash[end], bits) > 0)
		return PHB_FAIL;

	if (compare_U32(phonetic_hash[start], value, bits) == 0)
	{
		//strncpy((char*)ChLtr, phonetic_section[start], outMaxLen);
		*ChLtr = phonetic_section + phonetic_offset[start];
		return PHB_SUCCESS;
	}

	while (end - start > 1)
	{
		(compare_U32(phonetic_hash[middle], value, bits) < 0) ? (start = middle) : (end = middle);
		middle = (start + end) / 2;
	}

	if (compare_U32(phonetic_hash[++start], value, bits) == 0)
	{
		//strncpy((char*)ChLtr, phonetic_section[start], outMaxLen);
		*ChLtr = phonetic_section + phonetic_offset[start];
		return PHB_SUCCESS;
	}
	else 
		return PHB_FAIL;
}






